// splice untuk menambah, menghapus, ataupun mengganti elemen di posisi tertentu
const arr = [1, 2, 3];
// parameter pertama adalah index awal
// parameter kedua adalah jumlah elemen yang akan dihapus
// parameter ketiga adalah elemen opsional yang akan ditambahkan ke array
arr.splice(2, 1, 4);
